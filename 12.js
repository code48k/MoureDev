let resta = 2;
resta -= 2;
console.log(resta);

let multiplicador = 2;
multiplicador *= 2;
console.log(multiplicador);

let division = 2;
division /= 2;
console.log(division);

let modulo = 2;
modulo %= 2;
console.log(modulo);

let exponente = 2;
exponente **= 2;
